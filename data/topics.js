/* ============================================================
   TOPIC HELPERS
   ============================================================ */

window.TopicHelper = {
    /**
     * Get a random topic from a specific category
     * @param {string} category - Category key
     * @param {string} difficulty - Difficulty level: "easy", "medium", "hard", or "all"
     * @returns {Object} Topic object {text, difficulty}
     */
    getRandom: function (category, difficulty) {
        var pool = [];

        if (category === "all") {
            for (var key in window.TOPICS) {
                if (Object.prototype.hasOwnProperty.call(window.TOPICS, key)) {
                    pool = pool.concat(window.TOPICS[key]);
                }
            }
        } else if (window.TOPICS[category]) {
            pool = window.TOPICS[category].slice();
        }

        // Filter by difficulty
        if (difficulty && difficulty !== "all") {
            var filtered = [];
            for (var i = 0; i < pool.length; i++) {
                if (pool[i].difficulty === difficulty) {
                    filtered.push(pool[i]);
                }
            }
            pool = filtered;
        }

        if (pool.length === 0) {
            return { text: "Tidak ada topik tersedia untuk filter ini.", difficulty: null };
        }

        var randomIndex = Math.floor(Math.random() * pool.length);
        return pool[randomIndex];
    },

    /**
     * Get all available categories
     * @returns {string[]} Array of category keys
     */
    getCategories: function () {
        return Object.keys(window.TOPICS);
    },

    /**
     * Get count of topics per category
     * @returns {Object} Category counts
     */
    getCounts: function () {
        var counts = {};
        for (var key in window.TOPICS) {
            if (Object.prototype.hasOwnProperty.call(window.TOPICS, key)) {
                counts[key] = window.TOPICS[key].length;
            }
        }
        return counts;
    }
};
